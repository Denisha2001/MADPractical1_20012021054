fun main(){
    var a: Int=10   //Int to Double
    var b: Double=a.toDouble()
    println("Double value :$b")

    var c: String="23123"  //String to Int
    var d: Int=c.toInt()
    println("Integer value :$d")


    var e: String="1054"  //String to Double
    var f: Double=e.toDouble()
    println("Double value :$f")
}